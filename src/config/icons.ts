/**
 * Resolve an icon name (or raw SVG string) to its SVG markup.
 * If the input is a known icon name, returns the library SVG.
 * If the input is raw SVG (starts with "<svg"), passes through unchanged.
 * Otherwise returns an empty string (renders nothing).
 */
export function resolveIcon(input: string | undefined): string {
  if (!input) return '';
  if (input.trim().startsWith('<svg')) return input;
  return icons[input] ?? '';
}

/**
 * Render N filled stars as inline SVGs.
 * Used by Hero, TrustBar, Testimonials — anywhere a star rating appears.
 */
export function starsHtml(count = 5): string {
  return icons.star.repeat(count);
}
